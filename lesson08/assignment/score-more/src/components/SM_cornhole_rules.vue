    <h4>Cornhole Game Play</h4>
    <ol>
        <li>Two teams of two people, termed partners, shall play. Partners shall stand at opposite cornhole boxes on the same side, from the perspective of a third person, and face each other, so there is no advantage given to one team.</li>
        <li>Each cornhole team shall have 4 bags of one color.</li>
        <li>All 8 cornhole bags begin at one end.</li>
        <li>A coin flip shall determine which cornhole team has honors in round one.</li>
        <li>One of the partners on the team who has honors shall begin play by throwing a cornhole bag at the opposite cornhole box.</li>
        <li>A cornhole player may throw from anywhere behind the front of the cornhole box they are throwing from.</li>
        <li>Game continues by alternating throws between the two opponent players until all 8 cornhole bags have been thrown.</li>
        <li>If a cornhole bag hits the ground then bounces up onto the board, that bag shall be taken off the playing surface.</li>
        <li>The next round starts when the other cornhole player on the team, which has honors, throws their first cornhole bag.</li>
        <li>The cornhole game continues until one team reaches 21 or more points.</li>
    </ol>
    <h4>Cornhole Game Scoring</h4>
    <ol> 
        <li>The score shall be taken after all cornhole bags have been thrown for a round.</li>
        <li>Points shall be given as follows.</li>
            3 points for a cornhole bag that goes through the hole.
            1 point for a cornhole bag that is on the playing surface.
            1 point for a cornhole bag that is hanging into the hole.
            1 point for a cornhole bag that is hanging off the edge but not touching the ground.
            0 points for a cornhole bag that is on the playing surface, but also touching the ground.
            0 points for a cornhole bag that is hanging off the front edge and is resting on a cornhole bag that is on the ground (unless the cornhole bag on the ground can be removed without making the hanging bag fall to the ground, then one point is given).
        <li>The cornhole team with the highest round score, adds the difference of the two scores to their game score.</li>
        <li>The cornhole team who wins the round is given honors to throw first in the next round.</li>
        <li>If both cornhole teams have the same round score then the game score stays the same and honors stays with the team who had it the previous round.</li>
    </ol>
    <h4>Cornhole Game Fouls</h4>
    <ol>
        <li>A foul occurs when any of the following happens.</li>
        <li>A cornhole players foot goes past the foot foul line, termed a foot foul.</li>
        <li>A player goes out of turn.</li>
        <li>The player who caused the foul loses the throw when the foul occurred and the bag shall be removed if it landed on the playing surface.</li>
    </ol>
    
