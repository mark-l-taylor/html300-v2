    <p class="mb-0">
        A coin is tossed to decide who starts and thereafter the player to pitch first alternates with the end. Throws must be underarm. The first player
        throws both horseshoes at the opposite stake one after the other. The second player then does the same thing. The score for the end is then 
        calculated and the players play the next end by reversing direction and throwing at the other stake.
    </p>
    <p>
        Any horseshoe that completely surrounds the stake is called a "ringer" and scores three points unless cancelled out by an opponents ringer. If
        there is some doubt as to whether or not a horseshoe qualifies as a ringer, a straight edge should be placed against the open end of the horseshoe.
        If straight edge doesn't touch the stake, a ringer is scored. For such a simple game the scoring can seem complicated because of the way that 
        ringers are cancelled out. To desimplify this, the various options are listed below:
    </p>    
    <ol>
        <li>If no ringers are thrown, the nearest horseshoe to the stake counts one point.</li>
        <li>If both players throw a single ringer each, the ringers are cancelled out and the nearest of the other two horseshoes scores one point.</li>
        <li>Should both players score two ringers each, they cancel each other out and no points are scored.</li>
        <li>If one player manages one ringer but the other player pitches two ringers, three points are scored.</li>
        <li>If a single ringer has been scored that player wins three points plus an extra point if that player's other horseshoe is the closest of the remaining three.</li>
        <li>Finally, if a player achieves two ringers and the other player manages none, six points are scored.</li>
    </ol>
    <p>
        The first player to reach 21 points wins the game.
    </p>
    <p>
        Note - shoes that lean against the stake are not counted nearer than any other shoe that touches the stake.
    </p>
